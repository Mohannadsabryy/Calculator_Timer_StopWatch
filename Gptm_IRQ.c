/* Include std_types file */
#include "std_types.h"

/* Include tm4c123gh6pm_registers file */
#include "tm4c123gh6pm_registers.h"

/*=========================================================================================================================================
                                                    extern Variables
=========================================================================================================================================*/

extern uint8 Gptm_Flag; /* this flag was set by gptm handler */

/*=========================================================================================================================================
                                                    Handler Definition
=========================================================================================================================================*/

/*******************************************************************
 *
 * Handler Name: GPTM_Timer0_Handler.
 *
 * Description: this handler is used to set Gptm timer flag.
 *
 * Parameters: void
 *
 * Return: void
 *
********************************************************************/
void GPTM_Timer0_Handler(void){
    /* write 1 to clear flag */
    TIMER0_GPTMICR |= 0x1;
    /* set Gptm_Flag */
    Gptm_Flag=TRUE;
}
