/* Include std_types file */
#include "std_types.h"

/* Include tm4c123gh6pm_registers file */
#include "tm4c123gh6pm_registers.h"

/*=========================================================================================================================================
                                                    extern Variables
=========================================================================================================================================*/

extern uint8 Exti_Flag; /* this flag will be set by Exti handler */

/*=========================================================================================================================================
                                                      Handler Definition
=========================================================================================================================================*/

/*******************************************************************
 *
 * Handler Name: EXTI4_Handler.
 *
 * Description: this handler is used to set External interrupt flag.
 *
 * Parameters: void
 *
 * Return: void
 *
********************************************************************/
void EXTI4_Handler(void){
    /* Clear flag */
    GPIO_PORTF_ICR_REG |= (1<<4);
    /* Set External Interrupt Flag */
    Exti_Flag=TRUE;
}


